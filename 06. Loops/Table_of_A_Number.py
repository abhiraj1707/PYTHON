#Using While Loop
n = int(input("Enter the number : "))
i = 1
while i <= 10:
    print(i*n)
    i = i+1



n = int(input("Enter the n : "))
m = int(input("Enter the m : "))
i = 1
while i <= m:
    print(i*n)
    i = i+1



#Using For Loop
n = int(input("Enter the n : "))
for i in range (1, 11):
    print(i*n)



n = int(input("Enter the n : "))
m = int(input("Enter the m : "))
for i in range(1, m+1):
    print(i*n)